#include "ft_btree.h"
#include <stdlib.h>

t_btree	*btree_create_node(void *item)
{
	t_btree	*node;

	node = malloc(sizeof(t_btree) * 1);
	node->item = item;
	node->right = NULL;
	node->right = NULL;
	return (node);
}
