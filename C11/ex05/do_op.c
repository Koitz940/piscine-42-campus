#include "operators.h"

void	ft_do_op(int a, int b, char op)
{
	if (op == '-')
		minus(a, b);
	if (op == '+')
		plus(a, b);
	if (op == '*')
		times(a, b);
	if (op == '/')
		divide(a, b);
	if (op == '%')
		mod(a, b);
}
